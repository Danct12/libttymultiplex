#include <errno.h>
#include <internal/pane.h>

int tym_i_csq_soft_reset(struct tym_i_pane_internal* pane){
  if(pane->sequence.integer_count != 0){
    errno = ENOENT;
    return -1;
  }
  tym_i_pane_reset(pane);
  return 0;
}
